"""
    del() 删除元素执行位置的元素
"""
a = [100,200,999,300,400]
del a[2]
# [100, 200, 300, 400]
print(a)

"""
    pop()删除并返回指定位置元素，如果未指定位置则默认操作列表最 后一个元素。
"""
a = [10,20,30,40,50]
b1 = a.pop()
# [10, 20, 30, 40] 50
print(a,b1)

b2 = a.pop(1)
# [10, 30, 40] 20
print(a,b2)


"""
    remove()方法 删除首次出现的指定元素，若不存在该元素抛出异常。
"""
a = [10,20,30,40,50,20,30,20,30]
a.remove(20)
# [10, 30, 40, 50, 20, 30, 20, 30]
print(a)
a.remove(100)
# ValueError: list.remove(x): x not in list
print(a)