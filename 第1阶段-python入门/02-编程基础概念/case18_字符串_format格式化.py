"""
    字符串的格式化
        基本语法：通过{}和:来替换以前的%
        format函数可以接受不限个数的参数，为止可以不按顺序

"""

a = "我的名字是:{0},年龄是:{1},{0}是个软件大拿！"
b = a.format("林大侠",18)
# 我的名字是:林大侠,年龄是:18,林大侠是个软件大拿！
print(b)

aa = "我的名字是:{name},年龄是:{age}"
bb = aa.format(age=19, name="林大侠")
# 我的名字是:林大侠,年龄是:19
print(bb)

c = "我是{0},我喜欢数字{1:*^8}".format("林大侠","666")
# 我是林大侠,我喜欢数字**666***
print(c)

"""
    数字格式化
    ".2f" 保留两位小数
"""
d = "我是{0},我的存款有{1:.2f}"
dd = d.format("林大侠",876555.334444)
# 我是林大侠,我的存款有876555.33
print(dd)

